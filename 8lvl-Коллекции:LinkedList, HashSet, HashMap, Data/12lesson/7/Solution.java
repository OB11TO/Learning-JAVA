package com.javarush.task.task08.task0827;

import java.util.Date;

/*
Работа с датой
*/

public class Solution {
    public static void main(String[] args) {
        System.out.println(isDateOdd("MAY 1 2013"));
    }

    public static boolean isDateOdd(String date) {
        Date currentDate = new Date(date); // берется дата старая
        Date startDate = new Date(date);  // берется дата новая
        startDate.setDate(1);   // 1 число  1 января
        startDate.setMonth(0);
        long time = currentDate.getTime() - startDate.getTime();   // от нынешней даты сегодня отнимаем дату с нового года получаем млисеунды
        long days = time / 24 / 60 / 60000 + 1; //преобраовывем милисекунды в дни
        return days % 2 == 1;   
    }
}
